package qrcode

import (
	"bytes"
	"github.com/mdp/qrterminal/v3"
	"github.com/skip2/go-qrcode"
	"github.com/zf1976/vlog"
	"image"
	"image/png"
	"io"
	"os"
)

const Size = 256

type QrCode struct {
	content string
	img     image.Image
	genImg  bool
}

func NewQrCode(content string, genImg bool) *QrCode {
	qr := &QrCode{
		content: content,
		genImg:  genImg,
	}
	err := qr.generateQrCode()
	if err != nil {
		vlog.Errorf("generate qrcode error: %v", err)
		return nil
	}
	return qr
}

//goland:noinspection GoUnhandledErrorResult
func (qr *QrCode) generateQrCode() error {
	var err error
	var img image.Image
	code, err := qrcode.Encode(qr.content, qrcode.Medium, Size)
	if err != nil {
		return err
	}
	buf := bytes.NewBuffer(code)
	img, err = png.Decode(buf)
	if err != nil {
		return err
	}
	qr.img = img

	if qr.genImg {
		newPng, _ := os.Create("qrcode.png")
		defer newPng.Close()
		png.Encode(newPng, img)
	}

	return nil
}

func (qr *QrCode) Get() ([]byte, error) {
	encode, err := qrcode.Encode(qr.content, qrcode.Medium, Size)
	if err != nil {
		return nil, err
	}
	return encode, nil
}

func (qr *QrCode) Output(out ...io.Writer) {
	if len(out) == 0 {
		qrterminal.Generate(qr.content, qrterminal.L, os.Stdout)
	} else if len(out) > 0 {
		multiWriter := io.MultiWriter(out...)
		qrterminal.Generate(qr.content, qrterminal.L, multiWriter)
	}
}
